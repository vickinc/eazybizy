#!/usr/bin/env node

/**
 * Script to enable Google Calendar sync for existing anniversary events
 */

const { PrismaClient } = require('@prisma/client');

const prisma = new PrismaClient();

async function enableSyncForAnniversaryEvents() {
  try {
    console.log('🔍 Finding anniversary events that need sync enabled...');
    
    // Find all anniversary events that are not synced
    const anniversaryEvents = await prisma.calendarEvent.findMany({
      where: {
        isAutoGenerated: true,
        type: 'ANNIVERSARY'
      },
      select: {
        id: true,
        title: true,
        syncStatus: true,
        googleCalendarId: true
      }
    });

    // Filter the ones that need sync enabled
    const eventsNeedingSync = anniversaryEvents.filter(event => 
      event.syncStatus === 'LOCAL' || 
      !event.syncStatus || 
      !event.googleCalendarId
    );

    console.log(`📊 Found ${eventsNeedingSync.length} anniversary events that need sync enabled`);

    if (eventsNeedingSync.length === 0) {
      console.log('✅ All anniversary events already have sync enabled');
      return;
    }

    let updatedCount = 0;

    for (const event of eventsNeedingSync) {
      try {
        await prisma.calendarEvent.update({
          where: { id: event.id },
          data: {
            syncStatus: 'PENDING',
            googleCalendarId: 'primary'
          }
        });

        console.log(`   ✅ Enabled sync for: ${event.title}`);
        updatedCount++;
      } catch (error) {
        console.error(`   ❌ Failed to update ${event.title}:`, error.message);
      }
    }

    console.log(`\n🎉 Successfully enabled sync for ${updatedCount} anniversary events`);
    console.log(`\n📝 Next steps:`);
    console.log(`   1. Anniversary events will sync to Google Calendar on next sync cycle`);
    console.log(`   2. Or manually trigger sync by accessing calendar page with Google auth`);

  } catch (error) {
    console.error('❌ Script failed:', error);
  } finally {
    await prisma.$disconnect();
  }
}

// Run the script
enableSyncForAnniversaryEvents();